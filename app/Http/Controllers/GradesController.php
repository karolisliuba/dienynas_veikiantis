    {
        $grade = new Grade();

        $grade->student_id = $request->input('student_id');
        $grade->grade = $request->input('grade');
        $grade->lectures_id = $request->input('lecture_id');

        $grade->save();

        return redirect()->route('student.show', $grade->student_id);
    }

